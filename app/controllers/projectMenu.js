var args = arguments[0] || {};
function doClick1() {
		var win = Titanium.UI.createWindow({
			title: 'PROJECT 1',
		});
		var back=Titanium.UI.createButton({
			title:'Back',
			width: Ti.UI.SIZE,
			height: Ti.UI.SIZE,
			bottom : 10,
			left : 10
		});
			var info=Titanium.UI.createButton({
			title:'Project 1 Information Goes Here',
			width: Ti.UI.SIZE,
			height: Ti.UI.SIZE
		});
		win.add(info);
		win.add(back);
		win.open();
};
